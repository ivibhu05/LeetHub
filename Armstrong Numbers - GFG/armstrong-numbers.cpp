class Solution {
  public:
    string armstrongNumber(int n){
        // code here
        int num=0;
        int ans=n;
        while(n!=0)
        {
            int res=n%10;
            num+=res*res*res;
            n/=10;
            
        }
     return (num==ans? "Yes":"No");
    }
};

